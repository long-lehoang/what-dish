// ---------- HELPERS ----------

function makeIngredients(
  recipeId: string,
  items: [string, number | undefined, string | undefined][],
): Ingredient[] {
  return items.map(([name, amount, unit], i) => ({
    id: `${recipeId}-ing-${i}`,
    recipeId,
    name,
    amount,
    unit,
    isOptional: false,
    sortOrder: i + 1,
  }));
}

function makeSteps(recipeId: string, descriptions: string[]): Step[] {
  return descriptions.map((description, i) => ({
    id: `${recipeId}-step-${i}`,
    recipeId,
    stepNumber: i + 1,
    description,
    sortOrder: i + 1,
  }));
}

